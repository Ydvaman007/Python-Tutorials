# Conversion of tonnes in quintals and kg
Ton = int(input("Enter the number of Tonnes:-"))

# Tonnes to Quintals
quin = Ton * 10

# Tonnes to kilograms
kg = Ton * 1000

# Quintals to kilograms
kg2 = quin * 100

print(f"{Ton} Tonne in Quintals = {quin} Quintals, in kilogram = {kg} kg and {quin} Quintals in kilograms = {kg2} kg")